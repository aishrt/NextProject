"use client";

import React, { useEffect, useState } from "react";
import ContentWrapper from "@/components/Layout/Admin/ContentWrapper";
import { Metadata } from "next";
import "../../client/client.css";
import Box from "@mui/material/Box";
import Button from "@mui/material/Button";
import Typography from "@mui/material/Typography";
import PrivacyPolicy from "./privacy-policy/Privacy";
import ThankuStep from "./thanku-page/page";
import RoleBasicInformation from "./_WrongfulClaimsForms/RoleBasicInformation";
import AboutYourself from "./_WrongfulClaimsForms/AboutYourself";
import EmployeeDetails from "./_WrongfulClaimsForms/EmployeeDetails";
import TerminationDetails from "./_WrongfulClaimsForms/TerminationDetails";
import SupportingEvidence from "./_WrongfulClaimsForms/SupportingEvidence";
import { useSearchParams, useRouter } from "next/navigation";
import LinearProgress, {
  LinearProgressProps,
} from "@mui/material/LinearProgress";
import axios from "axios";

import CircularProgress from "@mui/material/CircularProgress";
import { Wrongful } from "@/types/Wrongful";
import HealthStatus from "./_WrongfulClaimsForms/HealthStatus";
import ImpactEvidence from "./_WrongfulClaimsForms/Impact&Evidence";
import ReviewForm from "./_WrongfulClaimsForms/ReviewForm";

